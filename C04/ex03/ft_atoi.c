int	ft_atoi(char *str)
{
	int g;
	int z;

	g = 1;
	z = 0;
	while (*str == '\t' || *str == '\n' || *str == '\v'
		|| *str == '\f' || *str == '\r' || *str == ' ')
		str++;
	while (*str == '-' || *str == '+')
	{
		if (*str == '-')
		{
			g = g * -1;
			str++;
		}
		else
			str++;
	}
	while (*str >= '0' && *str <= '9')
	{
		g = (z * 10) + (*str - '0');
		str++;
	}
	return (z * g);
}
