unsigned int	ft_strlcpy(char *dest, char *src, unsigned int size)
{
	unsigned int	uzunluk;
	unsigned int	i;

	uzunluk = 0;
	i = 0;
	while (src[uzunluk] != '\0')
		uzunluk++;
	if (size != 0)
	{
		while (src[i] != '\0' && i < (size - 1))
		{
			dest[i] = src[i];
			i++;
		}
		dest[i] = '\0';
	}
	return (uzunluk);
}
