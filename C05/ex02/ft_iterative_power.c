int	ft_iterative_power(int nb, int power)
{
	int	a;
	int	t;

	a = 0;
	t = 1;
	if (nb == 0 && power == 0)
		return (1);
	if (power == 0)
		return (1);
	if (power < 0)
		return (0);
	while (a < power)
	{
		t = t * nb;
		a++;
	}
	return (t);
}
